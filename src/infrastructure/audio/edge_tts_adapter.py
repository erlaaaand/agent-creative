class EdgeTTSAdapter(TextToSpeechPort):
    async def generate_audio(self, text: str, voice: str, output_path: str) -> None:
        try:
            communicate = edge_tts.Communicate(text, voice)
            await communicate.save(output_path)
        except Exception as e:
            raise AudioGenerationError(f"Gagal generate audio: {e}") from e

    async def get_available_id_voices(self) -> list[str]:
        try:
            voices = await edge_tts.list_voices()
            id_voices = [
                v["ShortName"]
                for v in voices
                if v.get("ShortName", "").startswith("id-")
            ]
            if id_voices:
                return sorted(id_voices)
            return sorted(_FALLBACK_ID_VOICES)
        except Exception:
            return sorted(_FALLBACK_ID_VOICES)

    async def get_all_voices(self) -> list[str]:
        try:
            voices = await edge_tts.list_voices()
            if voices:
                return sorted([v["ShortName"] for v in voices])
            return sorted(_ALL_KNOWN_VOICES)
        except Exception:
            return sorted(_ALL_KNOWN_VOICES)

    async def preview_voice(self, voice_name: str, temp_path: str) -> None:
        try:
            preview_text = "Halo, ini adalah contoh suara saya"
            communicate = edge_tts.Communicate(preview_text, voice_name)
            await communicate.save(temp_path)
            os.startfile(temp_path)
        except Exception as e:
            raise AudioGenerationError(f"Gagal preview suara: {e}") from e